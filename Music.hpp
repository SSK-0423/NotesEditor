#pragma once
#include <string>
#include "picojson.h"
class Music {
private:
	std::string str;
	//jsonファイル
	picojson::value json;

public:
	int musicHandle;
	//曲名
	std::string name;
	//BPM
	float bpm;
	//拍子
	int beat;

	//曲が読み込まれたかどうか
	bool isMusicLoad;
	//曲が再生中かどうか
	bool isPlay;
	//コンストラクタ
	Music();
	//曲のセット
	void SetMusic(const int& handle);
	//曲の読込
	void LoadMusic();
	//デストラクタ
	virtual ~Music();
	//曲の再生・停止
	void PlayMusic();
	//曲を最初から再生
	void RestartMusic();
	//曲の長さを取得
	int GetTotalTime();
	//現在の再生位置を取得
	float GetElapsedTime();

	void MusicTimeDraw();
};